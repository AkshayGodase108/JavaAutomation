package exc10_String;

public class Lab90_StringBuffer {
    public static void main(String[] args) {
        String s1 = "Akshay";
        String s2 = new String("Akshay");
        StringBuffer s3 = new StringBuffer("Akshay");
        StringBuilder s4 = new StringBuilder("Akshay");
        System.out.println(s1+"\n"+s2+"\n"+s3+"\n"+s4);
    }
}
