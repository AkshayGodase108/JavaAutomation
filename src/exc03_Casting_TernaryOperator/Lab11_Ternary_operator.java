package exc03_Casting_TernaryOperator;

public class Lab11_Ternary_operator {
    public static void main(String[] args) {
        //ternary operator (?:) is nothing but If else statement.
        int age = 10;
        String canivote = age>=18? "Yes, you can vote" : "no, you cant vote";
        System.out.println(canivote);
    }
}
